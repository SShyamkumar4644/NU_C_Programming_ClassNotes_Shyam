#include <stdio.h>

int main() {
    // Primitive data types
    int age = 25;           // Integer
    float height = 5.9;     // Floating-point number
    double weight = 70.5;   // Double-precision floating-point number
    char grade = 'A';       // Character

    // Printing the primitive data types
    printf("Age: %d\n", age);
    printf("Height: %.1f\n", height);
    printf("Weight: %.2f\n", weight);
    printf("Grade: %c\n", grade);

    return 0;
}
