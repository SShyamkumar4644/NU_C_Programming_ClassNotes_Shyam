#include <stdio.h>

int main() {
    int decimal = 10;   // Decimal representation

    // Display in different bases
    printf("Decimal: %d\n", decimal);    // Decimal (base-10)
    printf("Octal: %o\n", decimal);      // Octal (base-8)
    printf("Hexadecimal: %x\n", decimal); // Hexadecimal (base-16)

    return 0;
}
